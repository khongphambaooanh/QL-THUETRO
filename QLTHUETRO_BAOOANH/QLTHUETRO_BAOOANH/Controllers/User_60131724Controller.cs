        private QL_THUETRO_BOEntities11 db = new QL_THUETRO_BOEntities11();

        // GET: User_60131724
        public ActionResult Index()
        {
            if (Session["idUser"] != null)
            {
                return View();
            }
            else
            {
                return RedirectToAction("Login");
            }
